package gutta.prediction.analysis.consistency;

/**
 * This enumeration lists the options to enable or disable cross-component entity accesses as part of the {@link ConsistencyIssuesAnalysis}.
 */
public enum CheckCrossComponentAccesses {

    /**
     * Denotes that cross-component access checks should be performed.
     */
    YES,

    /**
     * Denotes that cross-component access checks should not be performed.
     */
    NO

}
